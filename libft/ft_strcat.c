#include "libft.h"

char	*ft_strcat(char *dest, char *s)
{
	int	i;
	int	j;

	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (s[j] != '\0')
	{
		dest[i + j] = s[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
